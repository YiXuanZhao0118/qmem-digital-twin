"""Backend sanity tests for the kind metadata manifest.

These run against `backend/data/kinds.json` which is generated by
`npm run export:kinds` from the frontend PhysicsPlugin registry. If
the manifest is missing or schema_version drifts, the loader raises
KindsManifestError — these tests assert the file is present,
parseable, and shape-consistent with what backend code expects.

Run via:
    cd backend && .venv/Scripts/python.exe -m pytest tests/test_kinds_manifest.py -v
"""
from __future__ import annotations

import pytest

from app.kinds_manifest import (
    KindsManifestError,
    asset_name_patterns,
    component_anchor_contracts,
    component_type_to_kind,
    element_kinds,
    load_manifest,
    physics_plugins,
)


class TestManifestLoad:
    def test_loads_without_error(self) -> None:
        manifest = load_manifest()
        assert isinstance(manifest, dict)

    def test_schema_version_is_1(self) -> None:
        manifest = load_manifest()
        assert manifest["schema_version"] == 1

    def test_has_generated_at_timestamp(self) -> None:
        manifest = load_manifest()
        assert isinstance(manifest["generated_at"], str)
        assert "T" in manifest["generated_at"]  # ISO-8601 format


class TestComponentTypeToKind:
    def test_covers_canonical_optical_kinds(self) -> None:
        mapping = component_type_to_kind()
        # Anchored to the kinds users actually place in the catalog.
        for ct in ("mirror", "aom", "lens_biconvex", "waveplate", "isolator"):
            assert ct in mapping, f"{ct} missing from manifest mapping"

    def test_horn_antenna_present(self) -> None:
        """Pre-P2 drift: horn_antenna was in 5 frontend tables but
        missing from backend OPTICAL_COMPONENT_TYPE_TO_KIND. After M4
        the manifest is the single source so this drift can't recur.
        """
        mapping = component_type_to_kind()
        assert mapping.get("horn_antenna") == "horn_antenna"

    def test_aliases_map_to_canonical_kind(self) -> None:
        mapping = component_type_to_kind()
        # `laser` and `laser_source` both → laser_source.
        assert mapping["laser"] == "laser_source"
        assert mapping["laser_source"] == "laser_source"
        # `sma_cable` legacy aliases rf_cable.
        assert mapping["sma_cable"] == "rf_cable"
        # `rf_generator` and `dds_ad9959_pcb` both → rf_source.
        assert mapping["rf_generator"] == "rf_source"
        assert mapping["dds_ad9959_pcb"] == "rf_source"


