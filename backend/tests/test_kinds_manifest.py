class TestPhysicsPlugins:
    def test_each_plugin_has_required_fields(self) -> None:
        for p in physics_plugins():
            assert "id" in p
            assert "display_name" in p
            assert "physics" in p
            phys = p["physics"]
            assert phys["element_kind"] == p["id"]
            assert isinstance(phys["anchors"], dict)
            assert isinstance(phys["default_params"], dict)
            assert phys["primary_domain"] in ("optical", "rf")
            assert phys["align_variant"] in (
                "translate_anchor_to_beam",
                "translate_and_bragg_rotate",
                "translate_anti_parallel",
                "none",
            )

    def test_align_tolerance_within_sensible_range(self) -> None:
        """Tolerance is always 0..100 mm. Even kinds with
        alignVariant='none' can have a non-zero tolerance because
        some have custom align UX (fiber's "Align End A/B" button,
        rf_cable / rf_switch custom snapping) that uses the same
        tolerance field. So we don't require zero — just a sane
        envelope so a typo (e.g. 25000 → meant 25) surfaces."""
        for p in physics_plugins():
            tol = p["physics"]["align_tolerance_mm"]
            assert 0 <= tol <= 100, f"{p['id']}: tolerance {tol} outside [0, 100]"


