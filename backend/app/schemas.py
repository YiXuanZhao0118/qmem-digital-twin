# --- Time-domain envelopes ---------------------------------------------------
#
# These are the per-physics-module time representations used by the new
# transient solver. See docs/PHYSICS_TIME_DESIGN.md for the rationale on why
# we use slowly-varying envelopes instead of carrier-resolved fields.


class PulseEnvelope(CamelModel):
    """Slowly-varying complex envelope of an optical field, two polarisations.

    Stored on a uniform grid `t = t0_ns + (dt_ps/1000)·n`, n = 0..n_samples-1.
    Each sample is the complex amplitude in √mW units, so |E|² is instantaneous
    power. The envelope is in the rotating frame of `carrier_thz`; the full
    field is `Re[E(t) · exp(-i·2π·carrier·t)]` for each polarisation.

    For continuous-wave (CW) beams set `is_cw=True` — the envelope collapses
    to a single complex amplitude per polarisation and `dt_ps`/`n_samples`
    are ignored (length-1 sample arrays still hold the constant value).
    """
    is_cw: bool = True
    t0_ns: float = 0.0
    dt_ps: float = Field(default=0.0, ge=0.0)
    n_samples: int = Field(default=1, ge=1)
    carrier_thz: float = Field(gt=0)
    e_x_re: list[float] = Field(default_factory=list)
    e_x_im: list[float] = Field(default_factory=list)
    e_y_re: list[float] = Field(default_factory=list)
    e_y_im: list[float] = Field(default_factory=list)

    @model_validator(mode="after")
    def check_array_lengths(self) -> "PulseEnvelope":
        n = self.n_samples
        for name, arr in (
            ("e_x_re", self.e_x_re),
            ("e_x_im", self.e_x_im),
            ("e_y_re", self.e_y_re),
            ("e_y_im", self.e_y_im),
        ):
            if len(arr) not in (0, n):
                raise ValueError(f"{name} length {len(arr)} != n_samples {n}")
        if not self.is_cw and self.dt_ps <= 0:
            raise ValueError("Non-CW pulse must have dt_ps > 0")
        return self


class RFSignal(CamelModel):
    """Slowly-varying RF signal envelope on a uniform time grid.

    For digital trigger channels use `carrier_hz=0` and `amplitude` ∈ {0, 1}.
    For analog modulation, `amplitude` is √W (so amp² is instantaneous power)
    and `phase_rad` is referenced to `carrier_hz`.
    """
    is_cw: bool = True
    t0_ns: float = 0.0
    dt_ns: float = Field(default=0.0, ge=0.0)
    n_samples: int = Field(default=1, ge=1)
    carrier_hz: float = 0.0
    amplitude: list[float] = Field(default_factory=list)
    phase_rad: list[float] = Field(default_factory=list)

    @model_validator(mode="after")
    def check_array_lengths(self) -> "RFSignal":
        if len(self.amplitude) not in (0, self.n_samples):
            raise ValueError("amplitude length must match n_samples")
        if len(self.phase_rad) not in (0, self.n_samples):
            raise ValueError("phase_rad length must match n_samples")
        if not self.is_cw and self.dt_ns <= 0:
            raise ValueError("Non-CW RF signal must have dt_ns > 0")
        return self


