class AssetAnchor(CamelModel):
    """A point of interest on a 3D asset, in body-local (Z-up) frame.

    Phase 4 of the schema unification (2026-05-07): formalises the
    previously raw-JSONB anchor blob into a typed Pydantic model. The
    new field names embed frame and unit so a future reader cannot
    misinterpret coordinates:

      position_mm_body_local : {x, y, z} in mm, body-local Z-up frame
      direction_body_local   : {x, y, z} unit vector, body-local frame
                               (only present on face / optical anchors)
      aperture_mm            : optional clear-aperture diameter in mm

    Backward compatibility: input deserialisation accepts the legacy
    field names (`localPosition`, `localDirection`) so a client/migration
    that hasn't been updated yet still validates. All new writes emit the
    new names. The alembic migration `0018_normalize_asset_anchors`
    rewrites every existing JSONB blob to the new shape.
    """

    id: AssetAnchorId
    position_mm_body_local: Vec3Mm = Field(
        ...,
        validation_alias="positionMmBodyLocal",
        serialization_alias="positionMmBodyLocal",
    )
    direction_body_local: Vec3Mm | None = Field(
        default=None,
        validation_alias="directionBodyLocal",
        serialization_alias="directionBodyLocal",
    )
    aperture_mm: float | None = None
    # Rectangular aperture (e.g. PBS / BS cube diagonal cement plane is
    # a rectangle, not a square). When both width + height are set the
    # editor + ray-tracer use them; when missing we fall back to
    # 2*aperture_mm x 2*aperture_mm. Independent of aperture_mm so an
    # anchor can carry both shapes simultaneously without ambiguity.
    aperture_width_mm: float | None = None
    aperture_height_mm: float | None = None
    # R3 (PHY Editor authority, 2026-05-17): explicit aperture geometry.
    # circle    → uses aperture_mm as radius
    # ellipse   → uses width/height as semi-axes
    # rectangle → uses width/height as full extents
    # Legacy rows without this field infer: W+H present → rectangle,
    # aperture_mm only → circle (matches pre-R3 ray-tracer behaviour).
    aperture_shape: Literal["circle", "ellipse", "rectangle"] | None = None
    name: str | None = None
    type: str | None = None
    # Physical coaxial connector at this anchor — gender + family.
    # Only meaningful for RF / TTL / Trigger anchors (rf_in / rf_out / ttl_in / trigger_in);
    # left null on optical anchors. Edited per-anchor in the PHY Editor
    # RF / Components view.
    connector_type: Literal["sma_male", "sma_female", "bnc_male", "bnc_female"] | None = None

    @model_validator(mode="before")
    @classmethod
    def _accept_legacy_field_names(cls, data: Any) -> Any:
        """Accept legacy `localPosition` / `localDirection` keys on
        input. The migration rewrites stored data, but client code or
        external imports that still send the old names should round-trip
        cleanly until they're updated. Output is always the new names.
        """
        if not isinstance(data, dict):
            return data
        out = dict(data)
        if "localPosition" in out and "positionMmBodyLocal" not in out and "position_mm_body_local" not in out:
            out["positionMmBodyLocal"] = out.pop("localPosition")
        else:
            out.pop("localPosition", None)
        if "localDirection" in out and "directionBodyLocal" not in out and "direction_body_local" not in out:
            out["directionBodyLocal"] = out.pop("localDirection")
        else:
            out.pop("localDirection", None)
        return out


