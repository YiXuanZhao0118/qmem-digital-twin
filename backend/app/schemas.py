def _accept_legacy_keys(data: Any, renames: tuple[tuple[str, str], ...]) -> Any:
    """Backward-compat translator for Phase 5 kindParams renames. If
    `data` is a dict, every (old → new) pair is applied: when `old` is
    present and `new` is not, `data[new]` is set to `data[old]` and `old`
    is dropped. When both are present, `old` is silently discarded so the
    new value wins.

    Used inside `model_validator(mode="before")` on each kind-params
    Pydantic class that participates in the unification.
    """
    if not isinstance(data, dict):
        return data
    out = dict(data)
    for old, new in renames:
        if old in out:
            if new not in out:
                out[new] = out.pop(old)
            else:
                out.pop(old, None)
    return out


