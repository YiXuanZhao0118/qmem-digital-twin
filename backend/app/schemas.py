class TaperedAmplifierAseSample(CamelModel):
    """ASE-only emission (no seed) at a given drive current.

    A real TA chip emits broadband ASE in BOTH directions when no seed is
    present — forward (out of the output port) and backward (out of the input
    port). The two powers don't have a fixed ratio; they depend on facet
    coatings and chip design. Sampling at a few representative currents lets
    the ray-tracer interpolate to the user's chosen drive current. We allow
    `drive_current_ma == 0` (TA off / threshold anchor; both powers ≈ 0),
    which is why this is `ge` not `gt`.
    """
    drive_current_ma: float = Field(ge=0)
    forward_power_mw: float = Field(ge=0)
    backward_power_mw: float = Field(ge=0)


class TaperedAmplifierGainSample(CamelModel):
    """Output power at a given (input_power, drive_current) operating point.

    With a seed present the ASE is partly suppressed (the gain medium is being
    extracted by the seed), so the backward power typically DROPS as input
    power rises while forward power saturates near the chip's saturation
    output. Sampling on a 2D grid (input × current) lets the ray-tracer
    bilinearly interpolate to live operating conditions. `drive_current_ma`
    is `ge=0` so the off-state can be anchored, matching the ASE table.
    """
    input_power_mw: float = Field(ge=0)
    drive_current_ma: float = Field(ge=0)
    forward_power_mw: float = Field(ge=0)
    backward_power_mw: float = Field(ge=0)


