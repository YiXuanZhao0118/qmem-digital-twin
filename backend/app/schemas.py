class QuantumTrace(CamelModel):
    """Density matrix ρ(t) for a small Hilbert space (typical N ≤ 64).

    `rho_real` and `rho_imag` are M × N × N nested lists where M = len(t_ns).
    Hermiticity is not enforced by the schema (solver responsibility).
    """
    t_ns: list[float] = Field(default_factory=list)
    rho_real: list[list[list[float]]] = Field(default_factory=list)
    rho_imag: list[list[list[float]]] = Field(default_factory=list)
    basis_labels: list[str] = Field(default_factory=list)

    @model_validator(mode="after")
    def check_shapes(self) -> "QuantumTrace":
        m = len(self.t_ns)
        if len(self.rho_real) != m or len(self.rho_imag) != m:
            raise ValueError("rho arrays must have length len(t_ns)")
        if m == 0:
            return self
        n = len(self.basis_labels) if self.basis_labels else len(self.rho_real[0])
        for matrix in self.rho_real + self.rho_imag:
            if len(matrix) != n or any(len(row) != n for row in matrix):
                raise ValueError(f"rho matrices must be {n}×{n}")
        return self


